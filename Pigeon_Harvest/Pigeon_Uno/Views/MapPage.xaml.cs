    private void ApplyTabletLayout(double width)
    {
        if (width <= 0)
        {
            return;
        }

        var compact = width < 820;
        MapToolbar.Spacing = compact ? 6 : 10;

        if (wpListPanel != null)
        {
            wpListPanel.MinWidth = compact ? 240 : 260;
            wpListPanel.MaxHeight = compact ? 220 : 260;
            wpListPanel.Padding = compact ? new Thickness(10) : new Thickness(12);
        }

        if (geofencePanel != null)
        {
            geofencePanel.MinWidth = compact ? 280 : 300;
            geofencePanel.MaxWidth = compact ? 300 : 340;
            geofencePanel.Margin = compact ? new Thickness(10) : new Thickness(20);
        }

        if (FindName("download_panel") is Border downloadPanel)
        {
            downloadPanel.MaxWidth = compact ? 320 : 360;
            downloadPanel.Padding = compact ? new Thickness(10) : new Thickness(12);
        }
    }

    private void ViewModel_PropertyChanged(object sender, System.ComponentModel.PropertyChangedEventArgs e)
    {
        if (e.PropertyName == nameof(ViewModel.VehiclePosition))
        {
            var vehiclePosition = ViewModel.VehiclePosition;
            if (vehiclePosition != null)
            {
                var now = DateTime.UtcNow;
                var lat = vehiclePosition.Latitude;
                var lon = vehiclePosition.Longitude;

                if ((now - _lastVehiclePositionUiUpdate).TotalMilliseconds < VehiclePositionUpdateIntervalMs)
                {
                    return;
                }

                if (_lastRenderedVehiclePosition.HasValue)
                {
                    var previous = _lastRenderedVehiclePosition.Value;
                    if (Math.Abs(previous.Lat - lat) < 1e-6 && Math.Abs(previous.Lon - lon) < 1e-6)
                    {
                        return;
                    }
                }

                DispatcherQueue.TryEnqueue(() =>
                {
                    mapControl.UpdateVehiclePosition(lat, lon);
                    _observabilityService?.Track("map.vehicle.render");

                    // Auto-follow if enabled
                    if (_isFollowingVehicle)
                    {
                        mapControl.SetCenter(lat, lon);
                    }
                });

                _lastVehiclePositionUiUpdate = now;
                _lastRenderedVehiclePosition = (lat, lon);
            }
        }
        else if (e.PropertyName == nameof(ViewModel.TotalDistance))
        {
            DispatcherQueue.TryEnqueue(() =>
            {
                // Sinkronkan label total jarak dengan ViewModel
                tbTotalDistance.Text = $"{ViewModel.TotalDistance:F2} km";
            });
        }
    }

    private void MapControl_DoubleTapped(object sender, Microsoft.UI.Xaml.Input.DoubleTappedRoutedEventArgs e)
    {
        // Get actual click position on map
        var clickPosition = e.GetPosition(mapControl);
        var (clickLat, clickLon) = mapControl.GetLatLonFromClick(clickPosition);
        
        Serilog.Log.Information("[MapPage] Double-click at screen position ({X}, {Y}) = Lat: {Lat}, Lon: {Lon}", 
            clickPosition.X, clickPosition.Y, clickLat, clickLon);
        
        // Check if in geofence drawing mode
        if (ViewModel.IsDrawingGeofence)
        {
            if (ViewModel.GeofenceType == Pigeon_Uno.Core.Models.GeofenceType.Polygon)
            {
                // Add geofence vertex at clicked position
                if (ViewModel.AddGeofenceVertexCommand.CanExecute((clickLat, clickLon)))
                {
                    ViewModel.AddGeofenceVertexCommand.Execute((clickLat, clickLon));
                    UpdateGeofenceVerticesList();
                    UpdateGeofenceOnMap();
                }
            }
            else if (ViewModel.GeofenceType == Pigeon_Uno.Core.Models.GeofenceType.Circular)
            {
                // Set geofence center at clicked position
                if (ViewModel.SetGeofenceCenterCommand.CanExecute((clickLat, clickLon)))
                {
                    ViewModel.SetGeofenceCenterCommand.Execute((clickLat, clickLon));
                    
                    // Update UI textboxes
                    if (tbGeofenceLat != null) tbGeofenceLat.Text = clickLat.ToString("F6");
                    if (tbGeofenceLon != null) tbGeofenceLon.Text = clickLon.ToString("F6");
                    
                    // Update geofence on map
                    UpdateGeofenceOnMap();
                    
                    // Update geofence info display
                    UpdateGeofenceInfo();
                    
                    // Enable send button
                    if (btnSendGeofence != null) btnSendGeofence.IsEnabled = true;
                    if (btnClearGeofence != null) btnClearGeofence.IsEnabled = true;
                    
                    // Update status
                    if (geofenceStatusText != null) geofenceStatusText.Text = "Geofence Active";
                    if (geofenceStatusDot != null) geofenceStatusDot.Fill = new SolidColorBrush(Color.FromArgb(0xFF, 0x10, 0xB9, 0x81)); // Success color
                    
                    Serilog.Log.Information("[MapPage] Geofence center set to Lat: {Lat}, Lon: {Lon}, Radius: {Radius}m", 
                        clickLat, clickLon, ViewModel.GeofenceRadius);
                }
            }
        }
        else
        {
            // Add Waypoint at clicked position
            if (ViewModel.AddWaypointCommand.CanExecute((clickLat, clickLon)))
            {
                ViewModel.AddWaypointCommand.Execute((clickLat, clickLon));
                _undoStack.Add((clickLat, clickLon));
                _redoStack.Clear();
                UpdateTotalDistance();
                UpdateWaypointsOnMap();
            }
        }
    }

    private void MapControl_Tapped(object sender, Microsoft.UI.Xaml.Input.TappedRoutedEventArgs e)
    {
        if (btnSingleTapWP?.IsChecked == true)
        {
            var position = e.GetPosition(mapControl);
