    }

    private void UpdateGeofenceParameters()
    {
        if (tbGeofenceRadius != null && double.TryParse(tbGeofenceRadius.Text, out double radius))
            ViewModel.SetGeofenceRadius(radius);

        if (tbGeofenceAltitude != null && double.TryParse(tbGeofenceAltitude.Text, out double altitude))
            ViewModel.SetGeofenceMaxAltitude(altitude);

        var selectedType = (cbGeofenceType?.SelectedIndex ?? 0) == 0
            ? Pigeon_Uno.Core.Models.GeofenceType.Circular
            : Pigeon_Uno.Core.Models.GeofenceType.Polygon;
        
        if (selectedType != ViewModel.GeofenceType)
        {
            ViewModel.SetGeofenceType(selectedType);
            if (geofencePointsBorder != null)
                geofencePointsBorder.Visibility = selectedType == Pigeon_Uno.Core.Models.GeofenceType.Polygon
                    ? Visibility.Visible : Visibility.Collapsed;
        }
    }

    private void GeofenceType_SelectionChanged(object sender, SelectionChangedEventArgs e)
    {
