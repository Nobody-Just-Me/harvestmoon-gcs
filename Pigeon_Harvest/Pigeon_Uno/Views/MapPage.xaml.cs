    /// <summary>
    /// Update waypoint markers on map
    /// </summary>
    private void UpdateWaypointsOnMap()
    {
        if (mapControl == null) return;

        // Clear existing waypoints
        mapControl.ClearWaypoints();

        // Add all waypoints to map
        foreach (var wp in ViewModel.Waypoints.OrderBy(w => w.Sequence))
        {
            mapControl.AddWaypointMarker(
                wp.Sequence,
                wp.Latitude,
                wp.Longitude,
                wp.Altitude,
                wp.Command.ToString()
            );
        }
    }

    /// <summary>
    /// Update geofence on map
    /// </summary>
    private void UpdateGeofenceOnMap()
    {
        if (mapControl == null || !ViewModel.IsGeofenceActive) return;

        if (ViewModel.GeofenceType == Pigeon_Uno.Core.Models.GeofenceType.Circular)
        {
            mapControl.SetGeofence(
                true,
                ViewModel.GeofenceCenterLat,
                ViewModel.GeofenceCenterLon,
                ViewModel.GeofenceRadius,
                null
            );
            
            // Update geofence info display
            UpdateGeofenceInfo();
        }
        else if (ViewModel.GeofenceVertices.Count >= 3)
        {
            var vertices = ViewModel.GeofenceVertices
                .Select(v => (v.Lat, v.Lon))
                .ToList();
            
            mapControl.SetGeofence(
                false,
                0, 0, 0,
                vertices
            );
            
            // Update geofence info display
            UpdateGeofenceInfo();
        }
    }

    /// <summary>
    /// Update geofence info display
    /// </summary>
    private void UpdateGeofenceInfo()
    {
        Serilog.Log.Debug("[MapPage] UpdateGeofenceInfo called - txtCenter: {Center}, txtArea: {Area}", 
            txtGeofenceCenter != null, txtGeofenceArea != null);
        
        if (txtGeofenceCenter == null || txtGeofenceArea == null)
        {
            Serilog.Log.Debug("[MapPage] Geofence info TextBlocks not found");
            return;
        }

        if (ViewModel.GeofenceType == Pigeon_Uno.Core.Models.GeofenceType.Circular)
        {
            // Update center info
            txtGeofenceCenter.Text = $"{ViewModel.GeofenceCenterLat:F6}, {ViewModel.GeofenceCenterLon:F6}";
            
            // Calculate and display area (πr²)
            double areaM2 = Math.PI * ViewModel.GeofenceRadius * ViewModel.GeofenceRadius;
            double areaKm2 = areaM2 / 1_000_000;
            txtGeofenceArea.Text = areaKm2 < 1 
                ? $"{areaM2:F0} m²" 
                : $"{areaKm2:F3} km²";
            
            Serilog.Log.Debug("[MapPage] Geofence info updated - Center: {Center}, Area: {Area}", 
                txtGeofenceCenter.Text, txtGeofenceArea.Text);
        }
        else if (ViewModel.GeofenceVertices.Count >= 3)
        {
            // Update vertices count
            txtGeofenceCenter.Text = $"{ViewModel.GeofenceVertices.Count} vertices";
            
            // Calculate polygon area (Shoelace formula)
            double area = CalculatePolygonArea(ViewModel.GeofenceVertices);
            double areaKm2 = area / 1_000_000;
            txtGeofenceArea.Text = areaKm2 < 1 
                ? $"{area:F0} m²" 
                : $"{areaKm2:F3} km²";
            
            Serilog.Log.Debug("[MapPage] Geofence polygon info updated - Vertices: {Count}, Area: {Area}", 
                ViewModel.GeofenceVertices.Count, txtGeofenceArea.Text);
        }
    }

    /// <summary>
    /// Calculate polygon area using Shoelace formula
    /// </summary>
    private double CalculatePolygonArea(System.Collections.ObjectModel.ObservableCollection<Pigeon_Uno.Core.Models.GeofenceVertex> vertices)
    {
        if (vertices.Count < 3) return 0;

        double area = 0;
        for (int i = 0; i < vertices.Count; i++)
        {
            int j = (i + 1) % vertices.Count;
            area += vertices[i].Lon * vertices[j].Lat;
            area -= vertices[j].Lon * vertices[i].Lat;
        }
        area = Math.Abs(area) / 2.0;
        
        // Convert from degrees² to meters² (approximate at equator)
        // 1 degree ≈ 111,320 meters
        area *= 111320.0 * 111320.0;
        
        return area;
    }

    /// <summary>
    /// Handle waypoint moved event from map control
    /// </summary>
    private void MapControl_WaypointMoved(object sender, SkiaMapControl.WaypointMovedEventArgs e)
    {
        // Find waypoint in ViewModel and update position
        var waypoint = ViewModel.Waypoints.FirstOrDefault(w => w.Sequence == e.Sequence);
        if (waypoint != null)
        {
            waypoint.Latitude = e.NewLat;
            waypoint.Longitude = e.NewLon;
            
            // Update total distance
            UpdateTotalDistance();
            
            Serilog.Log.Debug("[MapPage] Waypoint {Seq} updated to {Lat}, {Lon}", 
                e.Sequence, e.NewLat, e.NewLon);
        }
    }

    /// <summary>
    /// Handle geofence center moved event from map control
    /// </summary>
    private void MapControl_GeofenceCenterMoved(object sender, SkiaMapControl.GeofenceCenterMovedEventArgs e)
    {
        // Update ViewModel geofence center
        if (ViewModel.SetGeofenceCenterCommand.CanExecute((e.NewLat, e.NewLon)))
        {
            ViewModel.SetGeofenceCenterCommand.Execute((e.NewLat, e.NewLon));
            
            // Update UI textboxes
            tbGeofenceLat.Text = e.NewLat.ToString("F6");
            tbGeofenceLon.Text = e.NewLon.ToString("F6");
            
            // Update geofence info
            UpdateGeofenceInfo();
            
            Serilog.Log.Debug("[MapPage] Geofence center updated to {Lat}, {Lon}", 
                e.NewLat, e.NewLon);
        }
    }

    /// <summary>
    /// Handle geofence vertex moved event from map control
    /// </summary>
    private void MapControl_GeofenceVertexMoved(object sender, SkiaMapControl.GeofenceVertexMovedEventArgs e)
    {
        // Update ViewModel geofence vertex
        if (e.VertexIndex >= 0 && e.VertexIndex < ViewModel.GeofenceVertices.Count)
        {
            var vertex = ViewModel.GeofenceVertices[e.VertexIndex];
            vertex.Lat = e.NewLat;
            vertex.Lon = e.NewLon;
            
            // Update geofence info (area calculation)
            UpdateGeofenceInfo();
            
            Serilog.Log.Debug("[MapPage] Geofence vertex {Index} updated to {Lat}, {Lon}", 
                e.VertexIndex, e.NewLat, e.NewLon);
        }
    }

