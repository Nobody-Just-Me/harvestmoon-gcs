    private void MapPage_Loaded(object sender, RoutedEventArgs e)
    {
        ApplyTabletLayout(ActualWidth);

        // Subscribe events hanya sekali
        if (!_isInitialized)
        {
            _isInitialized = true;

            if (ViewModel != null)
            {
                ViewModel.PropertyChanged += ViewModel_PropertyChanged;
                _waypointsCollectionChangedHandler ??= (_, __) => RebuildWaypointDock();
                ViewModel.Waypoints.CollectionChanged += _waypointsCollectionChangedHandler;
            }

            mapControl.WaypointMoved += MapControl_WaypointMoved;
            mapControl.GeofenceCenterMoved += MapControl_GeofenceCenterMoved;
            mapControl.GeofenceVertexMoved += MapControl_GeofenceVertexMoved;

            // Initialize offline map service
            try
            {
                var offlineService = App.Current.Services.GetService<IOfflineMapService>();
                if (offlineService != null)
                {
                    mapControl.SetOfflineMapService(offlineService);
                }
            }
            catch (Exception ex)
            {
                Serilog.Log.Warning(ex, "[MapPage] Failed to initialize offline map service");
            }
        }

        // Inisialisasi tampilan map selalu dijalankan setiap kali halaman ditampilkan
        _ = DispatcherQueue.TryEnqueue(Microsoft.UI.Dispatching.DispatcherQueuePriority.Low, () =>
        {
            mapControl.SetCenter(-7.2754, 112.7947, 15);

            if (wpListPanel != null)
            {
                wpListPanel.Visibility = Visibility.Collapsed;
            }

            if (tbMarkersCount != null) tbMarkersCount.Text = $"Markers: {ViewModel?.Waypoints.Count ?? 0} ▲";
            if (wpEmptyMessage != null)
            {
                wpEmptyMessage.Visibility = ViewModel?.Waypoints.Count == 0 ? Visibility.Visible : Visibility.Collapsed;
            }

            _isWPDockHidden = true;
            RebuildWaypointDock();
            UpdateWaypointsOnMap();
        });
    }

    private void MapPage_Unloaded(object sender, RoutedEventArgs e)
    {
        if (ViewModel != null)
        {
            ViewModel.PropertyChanged -= ViewModel_PropertyChanged;
            if (_waypointsCollectionChangedHandler != null)
            {
                ViewModel.Waypoints.CollectionChanged -= _waypointsCollectionChangedHandler;
            }
        }

        mapControl.WaypointMoved -= MapControl_WaypointMoved;
        mapControl.GeofenceCenterMoved -= MapControl_GeofenceCenterMoved;
        mapControl.GeofenceVertexMoved -= MapControl_GeofenceVertexMoved;

        _isInitialized = false;
    }

