            var waypoint = _redoStack[_redoStack.Count - 1];
            _redoStack.RemoveAt(_redoStack.Count - 1);
            _undoStack.Add(waypoint);
            
            ViewModel.AddWaypointCommand.Execute((waypoint.Lat, waypoint.Lon));
            UpdateTotalDistance();
        }
    }

